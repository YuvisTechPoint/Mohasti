export function getProduct(handle: string): Product | undefined {
  return products.find((p) => p.handle === handle);
}

export function getProductsByCollection(collectionHandle: string): Product[] {
  return products.filter((p) => p.collection === collectionHandle);
}

export function searchProducts(query: string): Product[] {
  const q = query.toLowerCase().trim();
  if (!q) return products;
  return products.filter(
    (p) =>
      p.title.toLowerCase().includes(q) ||
      p.description.toLowerCase().includes(q) ||
      p.collection.toLowerCase().includes(q),
  );
}

export function getCartRecommendations(
  cartHandles: string[],
  limit = 4,
): Product[] {
  const excluded = new Set(cartHandles);
  const cartCollections = new Set(
    cartHandles
      .map((handle) => getProduct(handle)?.collection)
      .filter(Boolean) as string[],
  );

  const score = (product: Product) => {
    let value = 0;
    if (cartCollections.has(product.collection)) value += 4;
    if (product.promoLabel) value += 3;
    if (product.status === "new") value += 2;
    if (product.status === "pre_order") value += 1;
    if (product.image) value += 1;
    return value;
  };

  return products
    .filter(
      (product) =>
        !excluded.has(product.handle) && product.status !== "sold_out",
    )
    .sort((a, b) => score(b) - score(a))
    .slice(0, limit);
}
